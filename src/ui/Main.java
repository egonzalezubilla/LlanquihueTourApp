    private void btnReservaGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservaGuardarActionPerformed

        if (cmbReservaServicio.getSelectedItem() == null || cmbReservaGuia.getSelectedItem() == null || cmbReservaRecurso.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Debe seleccionar una opción en todos los campos.");
            return;
        }

        String servicioNombre = (String) cmbReservaServicio.getSelectedItem();
        ServicioTuristico servicio = gs.buscarServicioPorNombre(servicioNombre);

        if (servicio == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: El servicio seleccionado no es válido.");
            return;
        }

        if (listaTemporalRuts.size() != servicio.getCantidad()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: La cantidad de clientes ("
                    + listaTemporalRuts.size() + ") no coincide con la capacidad del servicio ("
                    + servicio.getCantidad() + ").");
            return;
        }

        int mes = cmbReservaMes.getSelectedIndex() + 1;
        String codigoReserva = "RES" + servicio.getCodigo() + mes;

        String nombreGuia = (String) cmbReservaGuia.getSelectedItem();
        String nombreRecurso = (String) cmbReservaRecurso.getSelectedItem();

        String rutGuia = gp.obtenerRutGuiaPorNombre(nombreGuia);
        String codigoRecurso = gr.obtenerCodigoRecursoPorNombre(nombreRecurso);

        ReservaTuristica nuevaReserva = new ReservaTuristica(codigoReserva, mes, rutGuia, codigoRecurso, servicio.getCodigo(), listaTemporalRuts);
        gres.registrarReserva(nuevaReserva, true);

        listaTemporalRuts.clear();
        javax.swing.JOptionPane.showMessageDialog(this, "Reserva guardada con éxito: " + codigoReserva);

        txtReservaCodigo.setText("");
        cmbReservaServicio.setSelectedIndex(0);
        cmbReservaGuia.setSelectedIndex(0);
        cmbReservaRecurso.setSelectedIndex(0);

        txtReservaCodigo.setVisible(false);
        btnReservaAgregar.setVisible(false);
        cmbReservaServicio.setVisible(false);
        cmbReservaGuia.setVisible(false);
        cmbReservaRecurso.setVisible(false);
        btnReservaGuardar.setVisible(false);
        btnReservaVer.setVisible(true);
        reservaServicio.setVisible(false);
        reservaGuia.setVisible(false);
        reservaRecurso.setVisible(false);
        reservaRut.setVisible(false);
    }//GEN-LAST:event_btnReservaGuardarActionPerformed

