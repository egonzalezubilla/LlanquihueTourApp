    private void btnRegistrarServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarServicioActionPerformed

        String codigo = txtServicioCodigo.getText();
        String nombre = txtServicioNombre.getText();
        String cantidadStr = txtServicioCantidad.getText();
        String tipo = (String) cmbServicio.getSelectedItem();
        String variable = txtServicioVariable.getText();

        if (!util.Validador.textoValido(codigo)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Código inválido.");
            return;
        }
        if (!util.Validador.textoValido(nombre)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nombre inválido.");
            return;
        }
        if (!util.Validador.numeroPositivoValido(cantidadStr)) {
            javax.swing.JOptionPane.showMessageDialog(this, "La cantidad de turistas debe ser un número entero positivo.");
            return;
        }

        int cantidad = Integer.parseInt(cantidadStr.trim());

        ServicioTuristico nuevoServicio = null;

        if ("Paseo Lacustre".equals(tipo)) {
            if (!util.Validador.textoValido(variable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "El campo " + servicioVariable.getText() + " es obligatorio.");
                return;
            }
            nuevoServicio = new PaseoLacustre(codigo, nombre, cantidad, variable);

        } else {
            if (!util.Validador.numeroPositivoValido(variable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "El campo " + servicioVariable.getText() + " debe ser un número entero positivo.");
                return;
            }
            int valorVariable = Integer.parseInt(variable.trim());

            if ("Excursion".equals(tipo)) {
                nuevoServicio = new Excursion(codigo, nombre, cantidad, valorVariable);
            } else if ("Tour gastronomico".equals(tipo)) {
                nuevoServicio = new TourGastronomico(codigo, nombre, cantidad, valorVariable);
            }
        }

        if (nuevoServicio != null) {
            gs.agregarServicio(nuevoServicio);
            javax.swing.JOptionPane.showMessageDialog(this, nuevoServicio.registrar());
        }

        txtServicioCodigo.setText("");
        txtServicioNombre.setText("");
        txtServicioCantidad.setText("");
        txtServicioVariable.setText("");
        cmbServicio.setSelectedIndex(0);

    }//GEN-LAST:event_btnRegistrarServicioActionPerformed

