    private void btnRegistrarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPersonaActionPerformed

        String rut = txtPersonaRut.getText();
        String nombre = txtPersonaNombre.getText();
        String telefono = txtPersonaTelefono.getText();
        String tipo = (String) cmbPersonas.getSelectedItem();
        String variable = txtPersonaVariable.getText();

        // 1. Validaciones comunes
        if (!util.Validador.rutValido(rut)) { //
            javax.swing.JOptionPane.showMessageDialog(this, "RUT inválido.");
            return;
        }
        if (!util.Validador.textoValido(nombre)) { //
            javax.swing.JOptionPane.showMessageDialog(this, "Nombre inválido.");
            return;
        }
        if (!util.Validador.telefonoValido(telefono)) { //
            javax.swing.JOptionPane.showMessageDialog(this, "Teléfono inválido (9 dígitos).");
            return;
        }

        if ("Cliente".equals(tipo)) {
            if (!util.Validador.numeroPositivoValido(variable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "La edad debe ser un número entero positivo.");
                return;
            }
        } else {
            if (!util.Validador.textoValido(variable)) {
                javax.swing.JOptionPane.showMessageDialog(this, "El campo " + personaVariable.getText() + " no puede estar vacío.");
                return;
            }
        }

        int telefonoInt = Integer.parseInt(telefono.trim());
        Persona nuevaPersona = null;

        if (tipo.equalsIgnoreCase("Cliente")) {
            int edad = Integer.parseInt(txtPersonaVariable.getText());
            nuevaPersona = new Cliente(rut, nombre, telefonoInt, edad);

        } else if (tipo.equalsIgnoreCase("Guia turistico")) {
            String capacitacion = txtPersonaVariable.getText();
            nuevaPersona = new GuiaTuristico(rut, nombre, telefonoInt, capacitacion);

        } else if (tipo.equalsIgnoreCase("Colaborador externo")) {
            String empresa = txtPersonaVariable.getText();
            nuevaPersona = new ColaboradorExterno(rut, nombre, telefonoInt, empresa);
        }

        if (nuevaPersona != null) {
            gp.agregarPersona(nuevaPersona);
            javax.swing.JOptionPane.showMessageDialog(this, nuevaPersona.registrar());
        }

        txtPersonaRut.setText("");
        txtPersonaNombre.setText("");
        txtPersonaTelefono.setText("");
        txtPersonaVariable.setText("");
        cmbPersonas.setSelectedIndex(0);

    }//GEN-LAST:event_btnRegistrarPersonaActionPerformed

